# if e else

# sintaxe: 
#   if + lógica:
#       código a ser executado 
#   else:
#       código a ser executado

# Exemplo de condicional simples if e else



# declaração de variável 
nota1 = 10
nota2 = 7

notaFinal = (nota1 + nota2) / 2 # Prcessamento de dados recebidos

media = 7 # Rega de negócio


# início da condição
if notaFinal <= media: # lógica da condição - true
    print('Aluno está reprovado')# Se verdadeiro será executado

else:# false
    print('aluno está aprovado')# será executado se a lógica for falsa